"""Generate contracts/src/DinoData.sol from the verified manifest.

Keeps the on-chain label/order/layout constants in lockstep with the encoder.
"""
import json
import os

from common import ROOT, VIS
from encoding import ATTR_ORDER, OUT_DIR

SOL_OUT = os.path.join(ROOT, "contracts", "src", "DinoData.sol")


def _blob(strings):
    # newline-joined; trait values never contain newlines
    joined = "\\n".join(strings)
    return f'"{joined}"'


def main():
    M = json.load(open(os.path.join(OUT_DIR, "manifest.json")))
    values = M["values"]
    cat_base = M["cat_base"]
    widths = M["widths"]
    orders = M["orders"]

    cat_idx = {c: i for i, c in enumerate(VIS)}
    attr_order_idx = [cat_idx[c] for c in ATTR_ORDER]

    lines = []
    lines.append("// SPDX-License-Identifier: CC0-1.0")
    lines.append("pragma solidity ^0.8.24;")
    lines.append("")
    lines.append("// AUTOGENERATED by build/gen_solidity.py — do not edit by hand.")
    lines.append("// Mirrors build/out/manifest.json (the verified encoding).")
    lines.append("library DinoData {")
    lines.append(f"    uint256 internal constant SUPPLY = {M['supply']};")
    lines.append(f"    uint256 internal constant N_COMPOSITE = {M['n_composite_sprites']};")
    lines.append(f"    uint256 internal constant N_UNIQUE = {M['n_unique_sprites']};")
    lines.append(f"    uint256 internal constant TOKEN_BYTES = {M['token_record_bytes']};")
    lines.append("")

    # category bit widths (VIS order)
    lines.append("    function widths() internal pure returns (uint8[9] memory w) {")
    for i, c in enumerate(VIS):
        lines.append(f"        w[{i}] = {widths[c]}; // {c}")
    lines.append("    }")
    lines.append("")

    # category global sprite base (VIS order)
    lines.append("    function catBase() internal pure returns (uint16[9] memory b) {")
    for i, c in enumerate(VIS):
        lines.append(f"        b[{i}] = {cat_base[c]}; // {c}")
    lines.append("    }")
    lines.append("")

    # render orders: orders[orderId] = 9 category indices, bottom->top
    lines.append(f"    function orderCount() internal pure returns (uint256) {{ return {len(orders)}; }}")
    lines.append("")
    lines.append("    function order(uint256 id) internal pure returns (uint8[9] memory o) {")
    for oid, ordr in enumerate(orders):
        idxs = [cat_idx[c] for c in ordr]
        cond = "if" if oid == 0 else "else if"
        lines.append(f"        {cond} (id == {oid}) {{")
        lines.append(f"            // {' > '.join(ordr)}")
        for k, v in enumerate(idxs):
            lines.append(f"            o[{k}] = {v};")
        lines.append("        }")
    lines.append('        else revert("order"); // unreachable with sealed, verified data')
    lines.append("    }")
    lines.append("")

    # attribute output order (indices into VIS), alphabetical
    lines.append("    function attrOrderIdx() internal pure returns (uint8[9] memory a) {")
    for k, v in enumerate(attr_order_idx):
        lines.append(f"        a[{k}] = {v}; // {ATTR_ORDER[k]}")
    lines.append("    }")
    lines.append("")

    # trait_type names (VIS order)
    lines.append("    function traitType(uint256 cat) internal pure returns (string memory) {")
    lines.append(f"        return _at({_blob(VIS)}, cat);")
    lines.append("    }")
    lines.append("")

    # value label lookup per category
    lines.append("    function catValue(uint256 cat, uint256 id) internal pure returns (string memory) {")
    for i, c in enumerate(VIS):
        cond = "if" if i == 0 else "else if"
        lines.append(f"        {cond} (cat == {i}) return _at({_blob(values[c])}, id);")
    lines.append('        revert("cat");')
    lines.append("    }")
    lines.append("")

    # minted-on and one-of-one
    lines.append("    function mintedOn(uint256 id) internal pure returns (string memory) {")
    lines.append(f"        return _at({_blob(M['minted_on'])}, id);")
    lines.append("    }")
    lines.append("")
    lines.append("    function oneOfOne(uint256 id) internal pure returns (string memory) {")
    lines.append(f"        return _at({_blob(M['one_of_one'])}, id);")
    lines.append("    }")
    lines.append("")

    # generic newline-delimited splitter
    lines.append("    // Returns the idx-th '\\n'-delimited field of `blob`.")
    lines.append("    function _at(string memory blob, uint256 idx) private pure returns (string memory) {")
    lines.append("        bytes memory b = bytes(blob);")
    lines.append("        uint256 start = 0;")
    lines.append("        uint256 field = 0;")
    lines.append("        for (uint256 i = 0; i <= b.length; i++) {")
    lines.append("            if (i == b.length || b[i] == 0x0a) {")
    lines.append("                if (field == idx) {")
    lines.append("                    bytes memory out = new bytes(i - start);")
    lines.append("                    for (uint256 j = start; j < i; j++) out[j - start] = b[j];")
    lines.append("                    return string(out);")
    lines.append("                }")
    lines.append("                field++;")
    lines.append("                start = i + 1;")
    lines.append("            }")
    lines.append("        }")
    lines.append('        revert("idx");')
    lines.append("    }")
    lines.append("}")

    os.makedirs(os.path.dirname(SOL_OUT), exist_ok=True)
    with open(SOL_OUT, "w") as f:
        f.write("\n".join(lines) + "\n")
    print(f"wrote {SOL_OUT} ({len(lines)} lines)")


if __name__ == "__main__":
    main()
